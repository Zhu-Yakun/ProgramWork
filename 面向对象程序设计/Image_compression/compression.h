class JpegEncoder {
private:
	struct BitString {
		int val;
		int len;
	};

	unsigned char* rgb;//图像的RGB信息
	unsigned int width;//图像的宽度
	unsigned int height;//图像的高度
	
	BitString Y_DC_Huff_Table[12];     //亮度直流哈夫曼树
	BitString Y_AC_Huff_Table[256];    //亮度交流哈夫曼树
	BitString CbCr_DC_Huff_Table[12];  //色度直流哈夫曼树
	BitString CbCr_AC_Huff_Table[256]; //色度交流哈夫曼树
	// 经过ZigZag之后的量化表
	unsigned char YQTable[64];
	unsigned char CbCrQTable[64];
public:
	// 构造函数
	JpegEncoder(unsigned char* data, unsigned int x, unsigned int y);
	// 析构函数
	~JpegEncoder();
	// 清理数据
	void clean();

	// 初始化量化表
	void InitQualityTables(int quality_scale);
	//初始化哈夫曼树函数
	void InitHuffmanTables();

	// 写jpg文件头
	void WriteHeader(ostream& out);
	// 反转字节序
	void WriteWord(std::ostream& out, unsigned short word);
	
	// 获取RGB
	void GetRGB(unsigned char(&R)[64], unsigned char(&G)[64], unsigned char(&B)[64], unsigned int x, unsigned int y);
	// RGB转为YCbCr
	void RGB_YCbCr(unsigned char(&R)[64], unsigned char(&G)[64], unsigned char(&B)[64], char(&y)[64], char(&cb)[64], char(&cr)[64]);
	// 离散余弦变换
	void DCT(char(&temp)[64], double(&data)[64]);
	// 根据哈夫曼表求解哈夫曼树
	void ComputeHuffmanTable(const char* NRCODES, const unsigned char* STD_TABLE, BitString* HuffmanTable);
	// 哈夫曼编码
	void HuffmanEncode(char* data, int& lastDC, BitString* HuffmanTableDC, BitString* HuffmanTableAC, BitString* outStr, int& StrCounts);
	// 获得bit编码
	BitString GetBitCode(int value);
	// 写bit串
	void WriteBitStr(ostream& out, BitString* outStr, int bitcounts, int& newByte, int& newByterPos);

	// 量化
	void Quantization(double(&ytemp)[64], double(&cbtemp)[64], double(&crtemp)[64]);
	// ZigZag扫描
	void ZigZag_Scan(char(&y)[64], char(&cb)[64], char(&cr)[64], double(&ytemp)[64], double(&cbtemp)[64], double(&crtemp)[64]);
	
	// 压缩函数
	void EncodeToJpeg(const char* DstFile, int quality_scale = 100);
};